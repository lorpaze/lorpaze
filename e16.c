//С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. В полученном списке определить количество слов, которые начинаются и заканчиваются на одну и ту же букву. Если таких слов нет - выдать соответствующее сообщение. Вывести на печать исходный список, найденные слова и их количество

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
int main(){
	char A[500];
	printf("Введите строку:\n");
	gets(A);
	puts(A);
	char *b = strtok(A, " \n");
	int n=0;
	printf("Слова, которые начинаются и заканчиваются на одну и ту же букву: ");
	while(b != NULL){
		int l = strlen(b);
		if(b[0]==b[l-1]){
			printf("%s ", b);
			n++;}
		b = strtok(NULL, " \n");}
	if(n==0){
		printf("такие слова отсутсвтуют");}
	else{printf("\nКоличество данных слов = %d", n);}
	return 0;}

/*
stdio.h: Для работы с функциями ввода-вывода (gets, puts, printf, scanf).
stdlib.h: Для стандартных функций языка C (в данном случае не используется, но включено для потенциального использования).
string.h: Для работы со строками (strlen, strtok).
Создается массив A длиной 500 символов для хранения введенной строки.
gets(A) считывает строку, введенную пользователем, и сохраняет её в массив A. Важно: использование gets небезопасно, так как оно не проверяет переполнение буфера. Лучше использовать fgets, чтобы избежать ошибок.
puts(A) выводит введенную строку на экран.
strtok используется для разделения строки A на слова. Она принимает два аргумента:
Первый — строка, которую нужно разделить (в данном случае A).
Второй — строка с разделителями (в данном случае пробел " " и символ новой строки "\n").
Функция возвращает указатель на первое слово строки. Если слов больше нет, возвращается NULL.
Переменная n используется для подсчета количества слов, которые начинаются и заканчиваются на одну и ту же букву.
Цикл while выполняется, пока функция strtok возвращает указатель на очередное слово.
strlen(b) вычисляет длину текущего слова b и сохраняет её в переменной l.
Условие if(b[0] == b[l-1]) проверяет, совпадают ли первая и последняя буквы слова:
b[0] — первый символ слова.
b[l-1] — последний символ слова (индексация массива начинается с 0).
Если условие выполняется, слово выводится на экран, а счетчик n увеличивается.
После обработки текущего слова программа вызывает strtok(NULL, " \n"), чтобы получить следующее слово.
Если переменная n равна 0 (в строке не было слов, начинающихся и заканчивающихся на одну и ту же букву), выводится сообщение: "такие слова отсутствуют".
Если такие слова найдены, выводится их количество.
Программа завершается, возвращая 0, что указывает на успешное выполнение.
*/
