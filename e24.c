// С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. Посчитать в строке все слова, которые начинаются на гласную букву. Если таких слов нет - выдать соответствующее сообщение. Вывести на печать исходную строку, найденные слова и их количество.

#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <stdbool.h>
bool gl(char *g){
	char G[]={'a', 'e', 'u', 'i', 'o', 'A', 'E', 'U', 'I', 'O'};
	size_t l = sizeof(G)/sizeof(G[0]);
	for(int i=0; i<l; i++){
		if (g[0]==G[i]){
			return true;}}
	return false;}

int main(){
	char A[500];
	printf("Введите строку:\n");
	gets(A);
	puts(A);
	char *b = strtok(A, " \n");
	int n=0;
	printf("Слова, начинающиеся на гласную: ");
	while(b!=NULL){
		if(gl(b)){
			n++; printf("%s ", b);}
		b=strtok(NULL, " \n");}
	if(n==0){
		printf("такие слова отсутствуют");}
	else{ printf(" (%d)", n);}
	return 0;}

