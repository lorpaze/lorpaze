// С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. Подсчитать все слова, длина которых меньше 4. Если таких слов нет - выдать соответствующее сообщение. Вывести на печать исходную. строку, найденные слова и их количество.

#include <stdio.h>
#include <string.h>
#include <stdlib.h>
int main(){
	char A[500]; int n=0;
	printf("Введите строку:\n");
	gets(A);
	puts(A);
	char *b = strtok(A, " \n");
	printf("\nСлова, в которых меньше 4-х символов: ");
	while(b != NULL){
		int l = strlen(b);
		if(l<4){
			n++;
			printf("%s ", b);}
		b = strtok(NULL, " \n");}
	if(n==0){printf("\nВсе слова имеют больше 4-х символов");}
	else{printf("\nКоличество слов, в которых меньше 4-х символов: %d", n);}
	return 0;}

/*
#include <stdio.h>: Подключение стандартной библиотеки для ввода-вывода (функции printf, gets, puts).
#include <string.h>: Подключение библиотеки для работы со строками (функции strlen, strtok).
#include <stdlib.h>: Подключение стандартной библиотеки (в данной программе не используется, можно убрать).
Функция main — точка входа в программу.
char A[500]: Создается массив символов длиной 500 для хранения введенной строки.
int n = 0: Переменная n используется для подсчета количества слов, в которых меньше 4 символов.
printf: Вывод приглашения для ввода строки.
gets(A): Считывает строку, введенную пользователем, и сохраняет ее в массив A. Важно: использование gets небезопасно, так как функция не проверяет длину вводимой строки. Лучше заменить на fgets.
puts(A): Выводит введенную строку на экран.
strtok(A, " \n"): Функция strtok делит строку A на слова, используя пробел (" ") и символ новой строки ("\n") в качестве разделителей.
Возвращает указатель на первое слово строки.
Если слов больше нет, возвращает NULL.
char *b: Указатель на текущее слово.
Выводится текст, который будет заголовком для списка слов, длина которых меньше 4 символов.
Цикл выполняется, пока b содержит указатель на очередное слово (т.е. пока есть слова для обработки).
strlen(b): Вычисляет длину текущего слова (количество символов в слове).
Результат сохраняется в переменной l.
Если длина текущего слова меньше 4 (l < 4):
n++: Увеличивается счетчик n, который отслеживает количество таких слов.
printf("%s ", b): Текущее слово выводится на экран.
Функция strtok вызывается снова для получения следующего слова.
Если слов больше нет, возвращается NULL, и цикл завершается.
После завершения цикла проверяется значение переменной n.
Если n == 0, это означает, что не нашлось слов длиной меньше 4 символов.
В этом случае программа выводит сообщение: "Все слова имеют больше 4-х символов."
Если n > 0 (т.е. есть слова длиной меньше 4 символов), программа выводит их количество.
Возвращение 0 указывает на успешное завершение программы.
*/