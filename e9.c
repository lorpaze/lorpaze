//С клавиатуры вводится последовательность целых чисел. Просмотреть последовательность и удалить компоненты, кратные 5. Если таковых нет – выдать соответствующее сообщение. Вывести на печать исходную и полученную последовательности.

#include <stdio.h>
#define MAX_SIZE 500
int main() {
int numbers[MAX_SIZE];
int result[MAX_SIZE];
int n, newSize = 0;
// Ввод количества чисел
printf("Введите количество чисел в последовательности (не более 500): ");
scanf("%d", &n);
// Проверка на корректность ввода
if (n <= 0 || n > MAX_SIZE) {
printf("Некорректное количество чисел.\n");
return 1;
}
// Ввод последовательности чисел
printf("Введите последовательность из %d целых чисел:\n", n);
for (int i = 0; i < n; i++) {
scanf("%d", &numbers[i]);
}
// Вывод исходной последовательности
printf("\nИсходная последовательность:\n");
for (int i = 0; i < n; i++) {
printf("%d ", numbers[i]);
}
printf("\n");
// Проверка и удаление чисел, кратных 5
for (int i = 0; i < n; i++) {
if (numbers[i] % 5 != 0) {
result[newSize++] = numbers[i]; // Добавляем число в новый массив, если оно не кратно 5
}
}
// Вывод результата
if (newSize == 0) {
printf("\nВ последовательности нет чисел, которые не кратны 5.\n");
} else {
printf("\nПолученная последовательность (без чисел, кратных 5):\n");
for (int i = 0; i < newSize; i++) {
printf("%d ", result[i]);
}
printf("\n");
}
return 0;
}

	
