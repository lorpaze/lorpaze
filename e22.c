// С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. Определить слово, содержащее наибольшее количество букв а и его номер в строке. Если такого слова нет - выдать соответствующее сообщение. Если таких слов несколько, то использовать первое найденное. Вывести на печать исходную строку, найденное слово, его номер и количество букв а в нем.

#include <stdio.h>
#include <string.h>
int main(){
	char A[500], *B[500]; int n=0, nmax=-1, max;
	printf("Введите строку:\n");
	gets(A);
	puts(A);
	char *b = strtok(A, " \n");
	while(b!=NULL){
		B[n++]=b;
		b=strtok(NULL, " \n");}
	int j=0, a=0;
	for(int i=0; i<n; i++){
		int l = strlen(B[i]);
		for(int j=0; j<l; j++){
			if(B[i][j]=='a'){
				a++;}}
		if(a>nmax){
			nmax=a;
			max=i;}
		a=0;}

	if(nmax==0){
		printf("Такого слова нет");}
	else {printf("Слово, содержащее наибольшее количество 'a': %s (%d штук)", B[max], nmax);}
	return 0;}

/*
char A[500]: Строковый массив длиной 500 для хранения введенной строки.
char *B[500]: Массив указателей, который будет хранить указатели на каждое слово из строки.
int n = 0: Счетчик количества слов в строке.
int nmax = -1: Переменная для хранения максимального количества символов 'a', найденного в словах (инициализируется значением -1).
int max: Индекс слова, в котором найдено максимальное количество символов 'a'.
strtok(A, " \n"): Функция strtok делит строку A на слова, используя пробелы (" ") и символы новой строки ("\n") в качестве разделителей.
Возвращает указатель на первое слово строки.
Если слов больше нет, возвращает NULL.
while (b != NULL): Цикл выполняется, пока есть слова.
B[n++] = b: Сохраняем указатель на текущее слово в массиве B и увеличиваем счетчик n (общее количество слов).
b = strtok(NULL, " \n"): Получаем следующее слово.
int j: Переменная для индекса символов в текущем слове.
int a = 0: Счетчик количества символов 'a' в текущем слове.
Цикл проходит по каждому слову в массиве B (от 0 до n - 1).
int l = strlen(B[i]): Определяем длину текущего слова B[i].
Вложенный цикл проходит по каждому символу текущего слова.
Если текущий символ равен 'a', увеличиваем счетчик a на 1.
Если количество символов 'a' в текущем слове больше текущего значения nmax:
nmax = a: Обновляем максимальное количество символов 'a'.
max = i: Сохраняем индекс текущего слова как слова с наибольшим количеством 'a'.
После завершения анализа слова сбрасываем счетчик a в 0.
Если nmax == 0, это означает, что ни в одном слове не было найдено символов 'a'. В этом случае выводится сообщение: "Такого слова нет".
Если nmax > 0, выводится слово с наибольшим количеством 'a' и их количество:
B[max]: Само слово.
nmax: Количество символов 'a' в этом слове.
*/
			



