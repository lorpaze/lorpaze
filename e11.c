//С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. Найти и удалить слова, длина которых меньше 5 символов. Если таковых нет - выдать соответствующее сообщение. Вывести на печать исходную и полученную последовательность слов.

#include <stdio.h>
#include <string.h>
#include <stdlib.h>
int main() {
	char A[500]; int i=0;
	printf("Введите слова через пробел:\n");
	gets(A);
	char *b = strtok(A, " /n");
	while(b != NULL){
		int l = strlen(b);
		if (l>5){
			i++;}
		if (l<5){
			printf("%s ", b);}
		b = strtok(NULL, " \n");}
	if(i==0){
		printf("\nНет слов удовлетворяющих условию");}
	return 0;
}

/*
#include <stdio.h>: Подключение стандартной библиотеки для ввода-вывода (функции printf, gets).
#include <string.h>: Подключение библиотеки для работы со строками (функции strlen, strtok).
#include <stdlib.h>: Подключение стандартной библиотеки (в данной программе не используется, можно убрать).
Инициализация переменных
char A[500]: Создается массив символов длиной 500 для хранения введенной строки.
int i = 0: Счетчик i используется для подсчета количества слов длиной больше 5 символов.
printf: Вывод приглашения для ввода строки.
gets(A): Считывает строку, введенную пользователем, и сохраняет ее в массив A. Важно: использование gets небезопасно, так как функция не проверяет длину вводимой строки. Лучше заменить на fgets.
strtok(A, " \n"): Функция strtok делит строку A на слова, используя пробел (" ") и символ новой строки ("\n") в качестве разделителей.
Возвращает указатель на первое слово строки.
Если слов больше нет, возвращает NULL.
char *b: Указатель на текущее слово.
Цикл выполняется, пока b содержит указатель на очередное слово (т.е. пока есть слова для обработки).
strlen(b): Вычисляет длину текущего слова (количество символов в слове).
Результат сохраняется в переменной l.
Если длина текущего слова больше 5 (l > 5), увеличивается счетчик i.
Это означает, что найдено слово с длиной больше 5 символов.
Если длина текущего слова меньше 5 (l < 5), слово выводится на экран.
Функция strtok вызывается снова для получения следующего слова.
Если слов больше нет, возвращается NULL, и цикл завершается.
После завершения цикла проверяется значение счетчика i.
Если i == 0, это означает, что не нашлось ни одного слова длиной больше 5 символов.
В этом случае программа выводит сообщение: "Нет слов удовлетворяющих условию".
*/