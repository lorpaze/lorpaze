//С клавиатуры вводится последовательность целых чисел. В полученном списке определить числа, значение которых больше 10. Если таких чисел нет - выдать соответствующее сообщение. Вывести на печать полученный список, найденные числа и их количество

#include <stdio.h>

int main() {
int numbers[100]; // Массив для хранения чисел
int n; // Количество чисел в последовательности
int count = 0; // Счётчик чисел > 10
// Ввод последовательности
printf("Введите количество чисел: ");
scanf("%d", &n);
printf("Введите последовательность целых чисел:\n");
for (int i = 0; i < n; i++) {
scanf("%d", &numbers[i]); // Считывание числа
}
// Вывод исходного списка чисел
printf("\nИсходный список чисел:\n");
for (int i = 0; i < n; i++) {
printf("%d ", numbers[i]);
}
printf("\n");
// Поиск чисел, которые больше 10
printf("\nЧисла, значение которых больше 10:\n");
for (int i = 0; i < n; i++) {
if (numbers[i] > 10) {
printf("%d ", numbers[i]);
count++; // Увеличиваем счётчик
}
}
// Проверка, найдены ли такие числа
if (count == 0) {
printf("Нет чисел, значение которых больше 10.\n");
} else {
printf("\n\nКоличество чисел больше 10: %d\n", count);
}
return 0;
}


