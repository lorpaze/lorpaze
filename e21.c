// С клавиатуры вводится символьная строка, состоящая из слов, разделенных пробелами. Определить слова в строке, которые начинаются на одну и ту же букву. Если таких слов нет - выдать соответствующее сообщение. Вывести на печать исходную строку, найденные слова с совпадениями и их количество. 

#include <stdio.h>
#include <string.h>
int main(){
	char A[500], *B[500]; int n=0; int L[26] = {0};
	printf("Введите строку:\n");
	gets(A);
	puts(A);
	char *b = strtok(A, " \n");
	printf("Слова начинающиеся на: ");
	while(b!=NULL){
		B[n]=b;
		n++;
		L[b[0]-'a']++;
		b=strtok(NULL, " \n");}
	int a=0;
	for(int i=0; i<26; i++){
		if(L[i]>1){
			a++;}}
	if(a==0){
		printf("такие слова отсутствуют");
		return 0;}
	printf("\n");
	for(int i=0; i<n; i++){
		if(B[i] != NULL && L[B[i][0]-'a']>1){
			printf("'%c' (%d): \n", B[i][0], L[B[i][0]-'a']);
			printf("%s\n", B[i]);
			for(int j=i+1; j<n; j++){
				if(B[j]!=NULL && B[i][0]==B[j][0]){
					printf("%s\n", B[j]);
					B[j]=NULL;
					}}
		B[i]=NULL;}}
			
	return 0;}


/*char A[500]: Строковый массив длиной 500 для хранения введенной строки.
char *B[500]: Массив указателей, который будет хранить указатели на каждое слово из строки.
int n = 0: Счетчик количества слов в строке.
int L[26] = {0}: Целочисленный массив длиной 26, где каждый элемент отвечает за количество слов, начинающихся с определенной буквы (индекс соответствует буквам латинского алфавита: a — индекс 0, b — индекс 1 и т.д.). Инициализируется нулями.
strtok(A, " \n"): Функция strtok делит строку A на слова, используя пробелы (" ") и символы новой строки ("\n") в качестве разделителей.
Возвращает указатель на первое слово строки.
Если слов больше нет, возвращает NULL.
char *b: Указатель на текущее слово.
Пока есть слова, цикл выполняется.
B[n] = b: Сохраняем указатель на текущее слово в массив B.
n++: Увеличиваем счетчик слов.
L[b[0] - 'a']++: Увеличиваем счетчик количества слов, начинающихся с буквы b[0] (первая буква текущего слова). Индекс определяется как b[0] - 'a', где 'a' — это код символа 'a' в ASCII.
b = strtok(NULL, " \n"): Получаем следующее слово.
int a = 0: Переменная a используется для подсчета букв, с которых начинаются два или больше слов.
Цикл проходит по массиву L (26 элементов, по количеству букв латинского алфавита).
Если для какой-либо буквы значение L[i] > 1 (т.е. есть два или больше слова, начинающихся с этой буквы), увеличиваем a.
Если a == 0, это означает, что нет букв, с которых начинаются два или больше слов.
В этом случае выводится сообщение: "такие слова отсутствуют", и программа завершает выполнение.
Внешний цикл проходит по всем словам в массиве B (от 0 до n - 1).
Условие if (B[i] != NULL && L[B[i][0] - 'a'] > 1):
Проверяется, что указатель на слово не NULL (это означает, что слово еще не обработано).
Проверяется, что количество слов, начинающихся с буквы B[i][0], больше 1.
Если условие выполнено:
Выводится буква слова и общее количество слов, начинающихся с этой буквы
Выводится первое слово
Внутренний цикл проходит по оставшимся словам (от i + 1 до n - 1):
Если слова начинаются с той же буквы (B[j][0] == B[i][0]), они выводятся
После обработки слово помечается как NULL
После обработки текущего слова B[i] оно тоже помечается как NULL
*/




